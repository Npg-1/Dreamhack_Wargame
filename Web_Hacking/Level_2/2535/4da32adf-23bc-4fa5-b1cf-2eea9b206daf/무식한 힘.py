async def fetch(session, i, url):
    # 2. i(questionId)에 맞는 정답 가져오기 (없으면 기본값 5)
    answer_value = ANSWERS.get(i, 5)
    
    payload = {
        "questionId": i,
        "answer": answer_value
    }
    headers = {"Content-Type": "application/json"}
    
    try:
        async with session.post(url, json=payload, headers=headers) as response:
            text = await response.text()
            status_code = response.status 
            return i, f"[{i}/722] (Ans: {answer_value}) Status: {status_code}, Response: {text}\n"
    except Exception as e:
        return i, f"Error at ID {i}: {e}\n"

async def main():
    url = "http://host3.dreamhack.games:14946/next"
    tasks = []
    
    # 세션 타임아웃 설정을 추가하면 더 안정적입니다.
    timeout = aiohttp.ClientTimeout(total=60)
    async with aiohttp.ClientSession(timeout=timeout) as session:
        for i in range(1, 723):
            tasks.append(fetch(session, i, url))
        
        # 병렬 실행
        responses = await asyncio.gather(*tasks)
        
    # ID 순서대로 정렬
    responses.sort(key=lambda x: x[0])
    
    with open("result.txt", "w", encoding="utf-8") as file:
        for _, content in responses:
            file.write(content)

    print(f"총 {len(responses)}개의 요청 완료. result.txt 확인.")

if __name__ == "__main__":
    asyncio.run(main())